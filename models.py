class JKNet(torch.nn.Module):
    def __init__(self, in_channels, hidden_channels, out_channels, num_layers,
                 dropout, args):
        super(JKNet, self).__init__()

        self.convs = torch.nn.ModuleList()
        self.convs.append(GCNConv(in_channels, hidden_channels, cached=True))
        for _ in range(num_layers - 2):
            self.convs.append(
            GCNConv(hidden_channels, hidden_channels, cached=True))
        self.convs.append(GCNConv(hidden_channels, out_channels, cached=True))

        self.dropout = dropout

        self.jk = JumpingKnowledge(mode='cat', channels=hidden_channels, num_layers=num_layers)
        self.args = args
        if args.dataset not in ['cora', 'citeseer', 'pubmed']:
            self.lin = torch.nn.Linear(hidden_channels*num_layers, hidden_channels)

        # self.alpha = torch.nn.Parameter(torch.FloatTensor(0.5))

    def reset_parameters(self):
        for conv in self.convs:
            conv.reset_parameters()

    def forward(self, x, adj_t):
        xs = []
        for conv in self.convs[:-1]:
            x = conv(x, adj_t)
            x = F.relu(x)
            x = F.dropout(x, p=self.dropout, training=self.training)
            xs += [x]
        x = self.convs[-1](x, adj_t)
        xs += [x]
        x = self.jk(xs)
        if self.args.dataset not in ['cora', 'citeseer', 'pubmed']:
            x = self.lin(x)
        return x


